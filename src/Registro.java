    private void registrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarseActionPerformed
        // Codigo para registrarse
        MySqlC mysql = new MySqlC();
        mysql.conn();
        String usuario = usuarioField.getText();
        String pass = passField.getText();
        String cpass = rpassField.getText();
        String seguridad = pseguridadField.getSelectedItem().toString();
        String respuesta = rpreguntaField.getText();
        existe = mysql.compruebaEnv(usuario);
        if ((usuario.trim().length()==0)||(seguridad.trim().length()==0)||(pass.trim().length()==0)||(cpass.trim().length()==0)){ JOptionPane.showMessageDialog(null, "Tienes que rellenar todos los campos" , "Error",JOptionPane.ERROR_MESSAGE); } 
        else if ((usuario.trim().length()<3)||(usuario.trim().length()>15)) { JOptionPane.showMessageDialog(null, "El usuario tiene que tener 3 caracteres como minimo y 15 como maximo" , "Error",JOptionPane.ERROR_MESSAGE);}
        else if ((pass.trim().length()<5)||(pass.trim().length()>15)) { JOptionPane.showMessageDialog(null, "La contrase침a tiene que tener 6 caracteres como minimo y 15 como maximo" , "Error",JOptionPane.ERROR_MESSAGE);}
        else if (!pass.equals(cpass)){ JOptionPane.showMessageDialog(null, "Las contrase침as no coinciden" , "Error",JOptionPane.ERROR_MESSAGE); }  
        else if (respuesta.trim().length()<4) { JOptionPane.showMessageDialog(null, "Las respuesta a la pregunta de seguridad tiene que ser mayor de 4 caracteres" , "Error",JOptionPane.ERROR_MESSAGE); }
        else if (existe == true){ JOptionPane.showMessageDialog(null, "El nombre de usuario ya existe, elige otro" , "Error",JOptionPane.ERROR_MESSAGE); }     
        else { mysql.accion("Insert into usuarios (usuario,pass,seguridad,respuesta) values ('"+usuario+"','"+pass+"','"+seguridad+"','"+respuesta+"')");
               JOptionPane.showMessageDialog(null, "Ya estas registrado, puedes loguearte" , "Registrado",JOptionPane.INFORMATION_MESSAGE);
               mysql.close();
               this.dispose();
        }
    }//GEN-LAST:event_registrarseActionPerformed

